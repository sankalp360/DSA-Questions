#include <iostream>
#include <string>
using namespace std;

void possibleSubsets(int n, int *arr)
{
    for (int i = 0; i < (1 << n); i++)
    {
        for (int j = 0; j < n; j++)
        {
            if (i & (1 << j))
            {
                cout << arr[j] << " ";
            }
        }
        cout << endl;
    }
}

int main()
{
    int n;
    cin >> n;

    int *arr = new int[n];

    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }

    possibleSubsets(n, arr);

    delete[] arr;
    arr = NULL;

    return 0;
}
